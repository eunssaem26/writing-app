export interface GrowthFocus {
  code: string;
  name: string;
  current: number;
  next: number;
  nextStep: string;
}

export interface GrowthGuidance {
  focus: GrowthFocus[];
  strength: { code: string; name: string; level: number } | null;
  maxed: boolean; // 모든 영역 7단계 도달
}

/**
 * 진단 결과로부터 성장 경로 안내를 생성한다.
 * - focus: 가장 약한 1~2개 영역(7단계 미만)과 그 영역의 "다음 단계에 할 수 있는 일"
 * - strength: 가장 강한 영역(격려용)
 */
export function generateGrowthGuidance(
  domainLevels: Record<string, number>,
  kind: Kind
): GrowthGuidance {
  const entries = DOMAINS[kind].map((d) => ({
    code: d.code,
    name: d.name,
    level: domainLevels[d.code] ?? 1,
  }));

  const improvable = [...entries]
    .filter((e) => e.level < 7)
    .sort((a, b) => a.level - b.level);

  const focus: GrowthFocus[] = improvable.slice(0, 2).map((e) => ({
    code: e.code,
    name: e.name,
    current: e.level,
    next: e.level + 1,
    nextStep: PROGRESSIONS[kind][e.code][e.level], // index=level → (level+1) 기술
  }));

  const strongest = entries.reduce((a, b) => (b.level > a.level ? b : a));

  return {
    focus,
    strength: { code: strongest.code, name: strongest.name, level: strongest.level },
    maxed: improvable.length === 0,
  };
}
